#include <stdio.h>

int main()
{
    int n;
    scanf("%d", &n);

    for (int i = 0; i < n; i++)
    {

        if (i == 0 || i == n - 1)
        {
            for (int j = 0; j < n * 2 - 3; j++)
            {
                printf("*");
            }
        }
        else
        {
            for (int f = 0; f < 3; f++)
            {
                if (f % 2 == 0)
                {
                    for (int g = 0; g < i; g++)
                    {
                        printf("*");
                    }
                }
                else
                {
                    for (int h = 2 * n - 5; h >= 2 * i - 1; h--)
                    {
                        printf(" ");
                    }
                }
            }
        }
        printf("\n");
    }
    return 0;
}
